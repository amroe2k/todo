// Show password change result (success/fail) using SweetAlert
function showPasswordMessage(type, title, message) {
  Swal.fire({
    icon: type,
    title: title,
    text: message,
    confirmButtonText: "OK",
    background: "#ffffff",
    color: "#1a1a1a",
  });
}
